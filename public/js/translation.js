let currentLang = localStorage.getItem("lang") || "en";

let buttonTitle = "Bahasa Indonesia";

function updateLanguage(selectedLang) {
  switch (selectedLang) {
    case "id":
      buttonTitle = "Bahasa Indonesia";
      currentLang = "id";
      break;
    case "en":
      buttonTitle = "English";
      currentLang = "en";
      break;
    default:
      buttonTitle = "Bahasa Indonesia";
      currentLang = "id";
  }

  localStorage.setItem("lang", currentLang);

  document.querySelectorAll("[data-lang-key]").forEach((el) => {
    const group = el.getAttribute("data-lang-group");
    const key = el.getAttribute("data-lang-key");
    el.innerHTML = translations[selectedLang][group][key]; // Update text
  });

  document.getElementById("selected-language-1").innerHTML = buttonTitle;
  document.getElementById("selected-language-2").innerHTML = buttonTitle;

  resetDropdown();
}

updateLanguage(currentLang);
